import UIKit
import Components
import ImagesService

public struct RadioViewStyle {
    
    public typealias Selection = RadioVariables.Selection
    public typealias State = RadioVariables.State
    
    private var variables: RadioVariables
    public var selection: Selection {
        get { variables.selection }
        set { variables.selection = newValue }
    }
    public var state: State {
        get { variables.state }
        set { variables.state = newValue }
    }
    
    public init(
        selection: Selection,
        state: State
    ) {
        self.variables = .init(selection: selection, state: state)
    }
    
    public func update(
        viewProperties: inout RadioView.ViewProperties
    ) {
        viewProperties.backgroundColor = variables.backgroundColor()
        viewProperties.size = .init(width: 20, height: 20)
        viewProperties.cornerRadius = viewProperties.size.width / 2
        viewProperties.borderWidth = variables.borderWidth()
        viewProperties.checkIcon = variables.checkIcon()
        viewProperties.borderColor = variables.borderColor()
        viewProperties.isUserInteractionEnabled = state != .disabled
    }
}

public struct RadioVariables {
    
    public enum Selection: String {
        case `default`
        case checked
    }
    
    public enum State: String {
        case `default`
        case pressed
        case disabled
    }
    
    public var selection: Selection
    public var state: State
    
    public init(
        selection: Selection,
        state: State
    ) {
        self.selection = selection
        self.state = state
    }
    
    public func backgroundColor() -> UIColor {
        let hexString = radio[selection.rawValue]?["background"]?["color"]?[state.rawValue]?["value"] ?? ""
        return UIColor(hexString: hexString)
    }
    
    public func borderColor() -> UIColor {
        let hexString = radio[selection.rawValue]?["border"]?["color"]?[state.rawValue]?["value"] ?? ""
        return UIColor(hexString: hexString)
    }
    
    public func borderWidth() -> CGFloat {
        switch selection {
        case .checked: 1
        case .default: 0
        }
    }
    
    public func checkIcon() -> UIImage? {
        let hexString = radio[selection.rawValue]?["checkIcon"]?["color"]?[state.rawValue]?["value"] ?? ""
        return .ic10Radio.withTintColor(UIColor(hexString: hexString))
    }
    
